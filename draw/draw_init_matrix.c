#include "../headers/draw.h"

t_matrix	ft_init_matrix(t_pt3d pos)
{
	t_matrix	m;

	m.x = ft_make_vector(0.0f, 0.0f, 0.0f);
	m.y = ft_make_vector(0.0f, 0.0f, 0.0f);
	m.z = ft_make_vector(0.0f, 0.0f, 0.0f);
	m.pos = pos;
	return (m);
}
